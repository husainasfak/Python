
# Integers

print(type(2))  # <class 'int'>

print(type(2-1)) # o/p - 1 <class 'int'>


# FLoat
print(type(2/4)) # o/p - 0.5 <class 'float'>

# Float take more space in the memory than int beacuse  everything store in the memory is binary and for strong floating number we need more than one location one location for before decimal point and another for after decimal point


print(type(20+1.1)) # <class 'float'>


print(2 ** 3) # 8 Power

print(5 // 4) # 1 Quotient


print(5 % 4) # 1 Remainder


